#include "ANO_Config.h"

ANO_Config ano;


ANO_Config::ANO_Config(void)
{
	f.STICKMODE = 1;
}

//指示灯
void ANO_Config::Pilot_Light(void)
{
	static u8 cnt = 0;
	
	if(f.ACCELMODE)	// 体感模式ACCELMODE
	{
		cnt++;
		switch(cnt)
		{
			case 1:
				led.ON();
				break;
			case 2:
				led.OFF();
				break;	
			case 4:
				cnt = 0;
				break;
		}
	}
	else	//普通模式STICKMODE
	{
		led.ON(); 
	}
	
}
